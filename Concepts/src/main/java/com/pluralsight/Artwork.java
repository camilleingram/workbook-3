package com.pluralsight;

public class Artwork {

    private String artist = "";
    private String name = "";
    private String medium = "";
    private double price = 0;

    public Artwork (String artist, String name, String medium, double price) {
        this.artist = artist;
        this.name = name;
        this.medium = medium;
        this.price = price;

    }


}
